    public void refresh(){
        
        DefaultTableModel model = (DefaultTableModel) this.tabela_tarefas.getModel();
        
        model.setRowCount(0);
        
        for (Tarefa tarefa : Main.sys.corrente.getProjetoPorNome(ProjetoFrame.projeto.getNome()).getTarefas()) {
            model.addRow(new Object[]{tarefa.getNome(),tarefa.getInicio(),tarefa.getPreco_h(),tarefa.getEstado(),tarefa.getFim()});
        }
    
    }

